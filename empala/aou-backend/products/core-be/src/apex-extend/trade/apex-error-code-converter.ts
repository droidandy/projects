export const convertErrorCode = (error: string): string => {
  for (const [code, handler] of Object.entries(errorCodeMap)) {
    if (error.startsWith(code)) return handler(error);
  }
  /* istanbul ignore next */
  throw new Error('Unable to translate order error, unknown code');
};

